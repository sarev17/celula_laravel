<?php

    function soma($valor1, $valor2){
        return $valor1 + $valor2;
    }

    function subtracao($valor1, $valor2){
        return $valor1 - $valor2;
    }

    function multiplicacao($valor1, $valor2){
        return $valor1 * $valor2;
    }

    function divisao($valor1, $valor2){
        return $valor1 / $valor2;
    }
?>